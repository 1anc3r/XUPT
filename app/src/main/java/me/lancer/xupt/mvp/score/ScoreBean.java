public class ScoreBean {

    private String scoreYear;
    private String scoreTerm;
    private String scoreName;
    private String scoreProperty;
    private String scoreGPA;
    private String scoreCredit;
    private String scoreValue;
    private String scoreResit;
    private String scoreRetake;

    public String getScoreYear() {
        return scoreYear;
    }

    public void setScoreYear(String scoreYear) {
        this.scoreYear = scoreYear;
    }

    public String getScoreTerm() {
        return scoreTerm;
    }

    public void setScoreTerm(String scoreTerm) {
        this.scoreTerm = scoreTerm;
    }

    public String getScoreName() {
        return scoreName;
    }

    public void setScoreName(String scoreName) {
        this.scoreName = scoreName;
    }

    public String getScoreProperty() {
        return scoreProperty;
    }

    public void setScoreProperty(String scoreProperty) {
        this.scoreProperty = scoreProperty;
    }

    public String getScoreGPA() {
        return scoreGPA;
    }

    public void setScoreGPA(String scoreGPA) {
        this.scoreGPA = scoreGPA;
    }

    public String getScoreCredit() {
        return scoreCredit;
    }

    public void setScoreCredit(String scoreCredit) {
        this.scoreCredit = scoreCredit;
    }

    public String getScoreValue() {
        return scoreValue;
    }

    public void setScoreValue(String scoreValue) {
        this.scoreValue = scoreValue;
    }

    public String getScoreResit() {
        return scoreResit;
    }

    public void setScoreResit(String scoreResit) {
        this.scoreResit = scoreResit;
    }

    public String getScoreRetake() {
        return scoreRetake;
    }

    public void setScoreRetake(String scoreRetake) {
        this.scoreRetake = scoreRetake;
    }
}
